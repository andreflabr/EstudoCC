void push(){
    struct Nodo *pNodo = (Nodo *)malloc(sizeof(struct Nodo));

    printf("Digite um numero\n");
    scanf("%d", &pNodo->num);

	pNodo->pProximo = pInicio;
	pInicio = pNodo;
}
////////////////////////////////////////////////////////////////////////////////////////////////
void imprimir(struct Nodo *pNodo) {
    if ( pNodo != NULL ) {
        printf("Numero: %d\n", pNodo->num);
    } else {
        printf("Vazio\n");
    }
}
